CREATE TABLE IF NOT EXISTS ACCOUNT_RECORD (
  ACCOUNT_ID VARCHAR(64) PRIMARY KEY,
  STATUS VARCHAR(32) NOT NULL
);

CREATE TABLE IF NOT EXISTS ACCOUNT_OPERATION (
  OPERATION_KEY VARCHAR(128) PRIMARY KEY,
  ACCOUNT_ID VARCHAR(64) NOT NULL,
  TARGET_STATUS VARCHAR(32) NOT NULL,
  REASON VARCHAR(255),
  EXECUTION_STATE VARCHAR(32) NOT NULL,
  FINAL_STATUS VARCHAR(32),
  TOOL_USE_ID VARCHAR(64),
  AUDIT_MESSAGE VARCHAR(255)
);